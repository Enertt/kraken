const TransferField = ({ currency, link }) => {
	const [value, setValue] = useState(link)
	const [isActive, setIsActive] = useState(false)
	const svg = getSvgForCurrency(currency, isActive)

	const handleChange = event => {
		setValue(event.target.value)
	}

	const handleInputClick = () => {
		setIsActive(true)
	}

	const handleInputBlur = () => {
		setIsActive(false)
	}

	return (
		<div className={isActive ? styles.blockActive : styles.block}>
			<div className={styles.currencySection}>
				{React.cloneElement(svg, { fill: isActive ? 'white' : '#7781A4' })}
				<p>{currency}</p>
			</div>
			<div className={styles.link}>
				<input
					type='text'
					value={value}
					onChange={handleChange}
					onClick={handleInputClick}
					onBlur={handleInputBlur}
				/>
			</div>
		</div>
	)
}

export default TransferField
